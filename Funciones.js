function sumar(a, b) {
    return a + b;
}
console.log(sumar(10, 20));

function restar(a, b) {
    return a - b;
}
console.log(restar(20, 10));

function multiplicar(a, b) {
    return a * b;
}
console.log(multiplicar(5, 4));

function dividir(a, b) {
    return a / b;
}
console.log(dividir(20, 5));

function convertirMayusculas(str) {
    return str.toUpperCase();
}
console.log(convertirMayusculas("hola"));


